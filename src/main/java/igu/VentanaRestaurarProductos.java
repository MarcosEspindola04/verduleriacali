    private static final java.util.logging.Logger logger = java.util.logging.Logger.getLogger(VentanaRestaurarProductos.class.getName());

     private JTable tablaInactivos;
    private JButton btnRestaurar;
    private DefaultTableModel modelo;
    private ControladoraProducto control;

    public VentanaRestaurarProductos() {
        setTitle("Productos Inactivos (Papelera)");
        setDefaultCloseOperation(DISPOSE_ON_CLOSE);
        setSize(600, 400);
        setLocationRelativeTo(null);

        control = new ControladoraProducto();

        modelo = new DefaultTableModel();
        modelo.setColumnIdentifiers(new String[]{"CÃ³digo", "Nombre", "Rubro", "Unidad", "Precio", "Cantidad"});

        tablaInactivos = new JTable(modelo);
        JScrollPane scrollPane = new JScrollPane(tablaInactivos);

        btnRestaurar = new JButton("Restaurar Producto");
        btnRestaurar.addActionListener(e -> restaurarSeleccionado());

        add(scrollPane, java.awt.BorderLayout.CENTER);
        add(btnRestaurar, java.awt.BorderLayout.SOUTH);

        cargarProductosInactivos();
    }

    private void cargarProductosInactivos() {
        modelo.setRowCount(0);
        List<Producto> productos = control.obtenerProductosInactivos();
        for (Producto p : productos) {
            modelo.addRow(new Object[]{
                p.getCodigo(),
                p.getNombre(),
                p.getRubro(),
                p.getUnidad(),
                p.getPrecio(),
                p.getCantidad()
            });
        }
    }

    private void restaurarSeleccionado() {
        int fila = tablaInactivos.getSelectedRow();
        if (fila >= 0) {
            int codigo = Integer.parseInt(tablaInactivos.getValueAt(fila, 0).toString());
            control.restaurarProducto(codigo);
            cargarProductosInactivos();
            JOptionPane.showMessageDialog(this, "Producto restaurado correctamente");
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione un producto para restaurar");
        }
    }

